import math

numero_mult_1 = int(input("Digite um valor entra 0 a 10: "))
dobro = numero_mult_1 * 2
print(f"valor do dobro é igual a {dobro}")
triplo = numero_mult_1 * 3
print(f"valor do triplo é igual a {triplo}")
quadrado = numero_mult_1 ** 2
print(f"valor do quadrado é igual a {quadrado}")
raiz = math.sqrt(numero_mult_1)

print(f"Propriedades do {numero_mult_1}\n Dobro: {dobro}\n Triplo:{triplo} \n Qudrado: {quadrado} \n raiz quadrada: {raiz:.2f}")

